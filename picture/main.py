a = 150
b = 90
dx = xcor()
dy = ycor()
begin_fill()
for deg in range(361):
    rad = math.radians(deg)
    x = a * math.sin(rad) + dx
    y = -b * math.cos(rad) + b + dy
    goto(x, y)
end_fill()
